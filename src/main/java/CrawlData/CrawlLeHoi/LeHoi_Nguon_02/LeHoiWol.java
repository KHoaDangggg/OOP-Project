package CrawlData.CrawlLeHoi.LeHoi_Nguon_02;

public class LeHoiWol {
    private String ten;
    private String anh;
    private String noiDung;

    public LeHoiWol(String ten, String anh, String noiDung) {
        this.ten = ten;
        this.anh = anh;
        this.noiDung = noiDung;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public String getAnh() {
        return anh;
    }

    public void setAnh(String anh) {
        this.anh = anh;
    }

    public String getNoiDung() {
        return noiDung;
    }

    public void setNoiDung(String noiDung) {
        this.noiDung = noiDung;
    }
}
